static void oled_cmd (uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };
    if (write (fd, buf, 2) != 2)
    {
        perror ("oled_cmd write");
    }
}

static void oled_send_data (const uint8_t *data, int len)
{
    uint8_t buf[17];
    buf[0] = 0x40;
    for (int i = 0; i < len; i += 16)
    {
        int chunk = len - i;
        if (chunk > 16)
            chunk = 16;
        memcpy (buf + 1, data + i, chunk);
        if (write (fd, buf, chunk + 1) != chunk + 1)
        {
            perror ("oled_send_data write");
        }
    }
}

int oled_init (void)
{
    fd = open (I2C_BUS, O_RDWR);
    if (fd < 0)
    {
        perror ("open " I2C_BUS);
        return -1;
    }
    if (ioctl (fd, I2C_SLAVE, OLED_ADDR) < 0)
    {
        perror ("ioctl I2C_SLAVE");
        close (fd);
        fd = -1;
        return -1;
    }
    static const uint8_t cmds[] = {
        0xAE,       // display OFF
        0xD5, 0x80, // set display clock divide / oscillator frequency
        0xA8, 0x3F, // set multiplex ratio (64 rows → 0x3F)
        0xD3, 0x00, // set display offset = 0
        0x40,       // set display start line = 0
        0x8D, 0x14, // enable charge pump
        0x20, 0x02, // memory addressing mode
        0xA1,       // segment re-map: col 127 → SEG0
        0xC8,       // COM scan direction: remapped (top→bottom)
        0xDA, 0x12, // COM pins hardware config: alt, no remap
        0x81, 0xCF, // set contrast
        0xD9, 0xF1, // set pre-charge period
        0xDB, 0x40, // set VCOMH deselect level
        0xA4,       // output follows RAM content
        0xA6,       // normal display (not inverted)
        0xAF        // display ON
    };
    for (size_t i = 0; i < sizeof (cmds); i++)
        oled_cmd (cmds[i]);

    oled_clear ();
    oled_flush ();
    return 0;
}

void oled_flush ()
{
    for (int page = 0; page < 8; page++)
    {
        oled_cmd (0xB0 | page);
        oled_cmd (0x02);
        oled_cmd (0x10);
        oled_send_data (&framebuffer[page * WIDTH], WIDTH);
    }
}

void oled_clear (void) { memset (framebuffer, 0x00, sizeof (framebuffer)); }

void oled_draw_char (int column, int page, char character)
{
    if (character < 32 || character > 'z')
        character = '?';
    const uint8_t *glyph = font5x7[(uint8_t)character - 32];
    for (int col = 0; col < 5; col++)
    {
        if (column + col >= WIDTH)
            break;
        framebuffer[page * WIDTH + column + col] = glyph[col];
    }
}

void oled_draw_string (int column, int page, const char *str)
{
    while (*str)
    {
        if (column + 6 > WIDTH)
        {
            column = 0;
            page++;
        }
        if (page >= PAGES)
            break;
        oled_draw_char (column, page, *str++);
        column += 6;
    }
}
void oled_close (void)
{
    if (fd >= 0)
        close (fd);
    fd = -1;
}